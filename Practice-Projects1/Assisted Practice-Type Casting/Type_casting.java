public class Type_casting {
	    public static void main(String[] args) {
	        System.out.println("Type Casting Program");
	        
	        System.out.println("Implicit type casting");
	        char a='Z';
	        int b=a;
	        System.out.println("Integer value = "+b);
	        
	        long c=a;
	        System.out.println("Long value = "+c);
	        
	        float d=a;
	        System.out.println("Float value = "+d);
	        
	        double e=a;
	        System.out.println("Double value"+e);
	        
	        System.out.println("\n");
	        
	        System.out.println("Explicit Type Casting");
	        
	        double f=90.5;
	        System.out.println("Double value = "+f);
	        
	        float g=(float)f;
	        System.out.println("Float value = "+g);
	        
	        long h=(long)f;
	        System.out.println("Long value = "+h);
	        
	        int i=(int)f;
	        System.out.println("Int value = "+i);
	        
	        char j=(char)f;
	        System.out.println("Char value = "+j);
	        
	    }
	}
	

